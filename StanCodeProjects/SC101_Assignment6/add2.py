import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    # 設定一個起點cur，因cur需要不斷連接下一個節點，另外設定head為要返回的起點地址
    cur = ListNode(0, None)
    head = cur
    total = 0
    # l1,l2,total有值就迴圈相加，若l1,l2為None則到串列的尾端，total為0則最後一位不用進位
    while l1 is not None or l2 is not None or total != 0:
        # 若l1或l2還有值就加入total並往下一個節點
        if l1 is not None:
            total += l1.val
            l1 = l1.next
        if l2 is not None:
            total += l2.val
            l2 = l2.next
        # 餘數連接為節點
        cur.next = ListNode(total % 10, None)
        # total為商，進位用
        total = total // 10
        # 將新的節點設為起點
        cur = cur.next
    #######################
    return head.next


